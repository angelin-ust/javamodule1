package oopsday.line1;

public class Function {
	public int x;
	public int y;
	public Function(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Function() {
	System.out.println(".");
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	

}
