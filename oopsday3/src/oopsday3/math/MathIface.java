package oopsday3.math;

public interface MathIface {
	public abstract double add(double a, double b);
	public abstract double sub(double a, double b);
	public abstract double mul(double a, double b);
	public abstract double div(double a, double b);
	public abstract int rem(int a, int b);
	public abstract void prime(int n);
	public abstract void oddeven(int n);
	public abstract void rev(int n);
	public abstract int factorial(int n);
	public abstract void posneg(int n);
}
